package org.judo.bean;

import java.util.Date;

public class Info {

	private String texte ;
	private Date   date ;
	private int    id ;
	
	
	public String getTexte() {
		return texte;
	}
	public void setTexte(String v) {
		this.texte = v;
	}
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date v) {
		this.date = v;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int v) {
		this.id = v;
	}
	
	
	
}
